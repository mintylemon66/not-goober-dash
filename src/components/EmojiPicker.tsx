interface EmojiPickerProps {
  selectedEmoji: string;
  onEmojiSelect: (emoji: string) => void;
  onClose?: () => void;
}

const EmojiPicker = ({ selectedEmoji, onEmojiSelect, onClose }: EmojiPickerProps) => {
  const [activeCategory, setActiveCategory] = useState("Smileys & People");

  return (
    <div className="bg-white rounded-lg shadow-lg p-4 max-w-md relative">
      {onClose && (
        <button
          onClick={onClose}
          className="absolute top-2 right-2 p-1 hover:bg-gray-100 rounded-full transition-colors"
          title="Close emoji picker"
        >
          <X size={16} className="text-gray-500" />
        </button>
      )}
      
      <div className="mb-4">
        <h3 className="text-lg font-semibold mb-2">Choose an Emoji</h3>
        <div className="flex flex-wrap gap-1 mb-3">
          {Object.keys(EMOJI_CATEGORIES).map((category) => (
            <button
              key={category}
              onClick={() => setActiveCategory(category)}
              className={`px-2 py-1 text-xs rounded transition-colors ${
                activeCategory === category
                  ? 'bg-blue-500 text-white'
                  : 'bg-gray-200 text-gray-700 hover:bg-gray-300'
              }`}
            >
              {category.split(' ')[0]}
            </button>
          ))}
        </div>
      </div>
      
      <div className="grid grid-cols-8 gap-1 max-h-48 overflow-y-auto">
        {EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES].map((emoji) => (
          <button
            key={emoji}
            onClick={() => onEmojiSelect(emoji)}
            className={`w-8 h-8 text-xl hover:bg-gray-100 rounded transition-colors ${
              selectedEmoji === emoji ? 'bg-blue-100 ring-2 ring-blue-500' : ''
            }`}
          >
            {emoji}
          </button>
        ))}
      </div>
    </div>
  );
};

export default EmojiPicker;
